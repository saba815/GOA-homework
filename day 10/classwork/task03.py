num1 = int(input("შეიყვანეთ პირველი რიცხვი: "))
num2 = int(input("შეიყვანეთ მეორე რიცხვი: "))

if num1 > num2:
    print("პირველი რიცხვი მეტია მეორეზე.")
else:
    print("პირველი რიცხვი არ არის მეტი მეორეზე.")

if num1 < num2:
    print("პირველი რიცხვი ნაკლებია მეორეზე.")
else:
    print("პირველი რიცხვი არ არის ნაკლები მეორეზე.")
