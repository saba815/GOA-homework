# ოჯახის წევრების ასაკები
father_age = int(input("შეიყვანე მამის ასაკი: "))
mother_age = int(input("შეიყვანე დედის ასაკი: "))
sibling_age = int(input("შეიყვანე და/ძმის ასაკი: "))


print("მამა იქნება", father_age + 25, "წლის შემდეგ 25 წლის მერე.")
print("დედა იქნება", mother_age + 25, "წლის შემდეგ 25 წლის მერე.")
print("და/ძმის იქნება", sibling_age + 25, "წლის შემდეგ 25 წლის მერე.")
