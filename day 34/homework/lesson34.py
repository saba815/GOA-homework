# List და string ორივე არის დალაგებული მიმდევრობები
# ეს ნიშნავს რომ მათში არსებული ელემენტებს აქვთ ინდექსები
# ინდექსები იწყება 0-დან და იზრდება მარცხნიდან მარჯვნივ
# ინდექსების გამოყენებით შეგვიძლია კონკრეტულ ელემენტზე წვდომა

# List არის ცვლადი (mutable)
# მისი ელემენტების შეცვლა, დამატება და წაშლა შესაძლებელია

# String არის უცვლელი (immutable)
# მის შიგთავსში პირდაპირი ცვლილება შეუძლებელია

# List შეიძლება შეიცავდეს სხვადასხვა ტიპის ელემენტებს (რიცხვი, ტექსტი, ლოგიკური მნიშვნელობა და სხვ.)
# String ყოველთვის შედგება მხოლოდ სიმბოლოებისგან

# ორივე სტრუქტურა საშუალებას გვაძლევს გამოვიყენოთ slicing (ნაწილის ამოჭრა)
# ორივეში შეგვიძლია წვდომა for ციკლით

# ძირითადი განსხვავება ის არის, რომ list-ს ვცვლით — string-ს ვერა





# Slicing ნიშნავს დალაგებული მიმდევრობის (მაგ. list, string) ნაწილის ამოჭრას

# Slicing-ის დროს ვიყენებთ სინტაქსს [საწყისი:საბოლოო]

# მიღებული შედეგი მოიცავს ელემენტებს საწყისი ინდექსიდან,
# მაგრამ არ მოიცავს საბოლოო ინდექსზე მყოფ ელემენტს

# Slicing საშუალებას გვაძლევს მივიღოთ კონკრეტული მონაკვეთი
# მონაცემთა წყაროდან, ზედმეტი ოპერაციების გარეშე

# ასევე შესაძლებელია slicing-ში უარყოფითი ინდექსების გამოყენება
# რაც ითვლის ელემენტებს ბოლოდან

# Slicing გამოიყენება როგორც string-ებზე, ასევე list-ებზე და სხვა მიმდევრობებზე


numbers = list(range(1, 21))      
sliced = numbers[4:10]            
print(sliced)                     

bodies = ["მზე", "მთვარე", "დედამიწა", "მარსი", "იუპიტერი", "სატურნი"]

even_indexed = bodies[::2]   
print(even_indexed)


numbers = list(range(16))     
odd_indexed = numbers[1::2]   
print(odd_indexed)


full_name = "დედა ბავშვი"  

first_name = full_name[:4]  
last_name = full_name[5:]   

print(first_name)  
print(last_name)   
